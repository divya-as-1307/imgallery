var images1 = document.querySelector(".images1")
var overlay =document.querySelector(".overlay")
var imgone = document.querySelector(".img1")
images1.addEventListener("click",function(){
overlay.classList.remove("hidden")
imgone.style.display= "block"
})
var closex1 = document.querySelector(".closex1")
closex1.addEventListener("click",function(){
    overlay.classList.add("hidden")
    imgone.style.display="none"
})          
